   <script>
	function addcontact() {
		var name = $('#contactName').val();
		var email = $('#contactEmail').val();
		var subject = $('#contactSubject').val();
		var message = $('#contactMessage').val();

		$.ajax({
			url:"/contactadmin",
			type:"post",
			data:{
				'username':name,
				'useremail':email,
				'usersubject':subject,
				'usermessage':message,
				"_token":"{{csrf_token()}}"
			},
			success:function(response){
				if(response == 'recorded'){
					Swal.fire({
				title: 'Success !',
				text: 'Your response has been recorded',
				icon: 'success',
				confirmButtonText: 'close'
				})
				}
				else{
					Swal.fire({
				title: 'Error !',
				text: 'There was an issue recording your response',
				icon: 'error',
				confirmButtonText: 'close'
				})
				}
			}
		})
	}
   </script>

